#Tipo de dato entero o largo (int)
num1 = 15
str(num1)
print(num1,type(num1))

#Tipo de dato flotante (float)
numfloat = 15.5
print(numfloat,type(numfloat))

#Tipo de dato complejo (complex)
numcomplex = 5 + 6j
print(numcomplex,type(numcomplex))

#Tipo de dato texto (String) 
nombre = 'Sebastian'
print(nombre,type(nombre))

#Tipo de dato Booleano (boolean)
true_false = 3 == 3
print(true_false,type(true_false))

##Metodo para centrar texto
nombre = "George"
print(nombre.center(30, "."))

##Metodo para colocar en la izquierda un texto
nombre2 = "Paul Peterson"
print(nombre2.ljust(30, "."))

##Metodo para colocar en la derecha un texto
nombre3 = "Mike White"
print(nombre3.rjust(30, "."))


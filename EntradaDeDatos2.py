#Variable que guarda la entrada de texto por parte de un usuario
name = input("Cual es tu nombre: ")
#Funcion print para imprimir texto concatenado
print("Hola " + name + ", vamos a realizar una suma")

#Variables que guardan la entrada de numeros enteros de un usuario (Gracias al comando int)
num1 = int(input("Digite un numero: "))
num2 = int(input("Digite otro numero: "))

#Variable que suma num1 con num2
res = num1 + num2
#Funcion print para imprimir texto concatenado
print(name + ", el resultado de la suma es: ", res)
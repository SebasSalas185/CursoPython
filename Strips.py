#Metodo .strip() (quita caracteres al inicio y al final)
cadena = "\nHola Sebas\n"
cadena= cadena.strip("\n")
print(cadena)
print("Como estas")

#Metodo .rstrip (quita caracteres al final)
cadena2 = "\nHola David\n"
cadena2= cadena2.rstrip("\n")
print(cadena2)
print("Como estas")


#Metodo .lstrip (quita caracteres al inicio)
cadena3 = "\nHola Marcela\n"
cadena3= cadena3.lstrip("\n")
print(cadena3)
print("Como estas")
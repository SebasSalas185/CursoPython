#Estos son los diferentes operadores aritmeticos que se pueden usar en Python
print("Suma:")
num1 = 5
num2 = 4
#Operador  Aritmetico "+"
res = num1 + num2
print("El resultado de la suma es: " + str(res))

print("Resta:")
num1 = 10
num2 = 5
#Operador  Aritmetico "-"
res = num1 - num2
print("El resultado de la resta es: " + str(res))

print("Multiplicacion:")
num1 = 10
num2 = 2
#Operador  Aritmetico "*"
res = num1 * num2
print("El resultado de la multiplicacion es: " + str(res))

print("Exponente:")
num1 = 2
exp = 5
#Operador  Aritmetico "**"
res = num1 ** exp
print("El resultado del exponente es: " + str(res))

print("Division:")
num1 = 4
num2 = 2
#Operador  Aritmetico "/"
res = num1 / num2
#Funcion print para imprimir texto con variable res en pantalla
print("El resultado de la division es: " + str(res))

print("Modulo:")
num1 = 30
num2 = 8
#Operador  Aritmetico "%"
res = num1 % num2
#Funcion print para imprimir texto con variable res en pantalla
print("El resultado del modulo es: " + str(res))

print("Division Entera:")
num1 = 4
num2 = 2
#Operador  Aritmetico "//"
res = num1 // num2
print("El resultado de la division entera es: " + str(res))
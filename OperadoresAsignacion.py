#Variable con un valor asignado
x = 5

print("Incremento y Decremento de una variable\n")

print("Incremento o Aumento:")
print("El valor inicial de x es:", x)
#El operador "+=" agrega otro valor a la variable 
x += 1
print("El valor incrementado de x es:", x)

print("\nDecremento o Disminucion:")
print("El valor inicial de x es:", x)
#El operador "+=" resta un valor a la variable 
x -= 1
print("El valor disminuido de x es:", x)